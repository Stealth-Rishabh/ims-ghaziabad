                                    <p class="text-justify mt-3">The Marketing Club of IMS, MARKWIZ, aims to create a thriving community of marketing enthusiasts, where like-minded individuals come together to share their knowledge and expertise, crafting a space for limitless growth
                                        and development.</p>
                                    <p class="mt-3 text-justify">At MARKWIZ, the objectives are committed to deliver the benefits and numerous opportunities to its members where innovation and the thrill of marketing excellence are experienced. To enhance the marketing journey, a
                                        multitude of benefits is extended to club members:</p>

                                    <div class="listing_Dv">
                                        <ul>
                                            <li>Exclusive Insights into the Evolving Marketing Landscape.</li>
                                            <li>Hands on Live marketing projects.</li>
                                            <li>Networking with Invaluable Connections that shape Marketing Careers.</li>
