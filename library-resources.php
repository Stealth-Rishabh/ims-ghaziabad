                                <h3 class="text-white fs-4">Library Management Software (LMS)</h3>
                            </div>
                            <div class="d-flex justify-content-start flex-wrap case__flex pt-4">

                                <p>In the Institute, most library operations and services are automated. An online public access catalog (Web OPAC) is available for users. Twenty-four dedicated nodes with monitors, one laser printer, and one scanner & other peripherals are available to facilitate using the library materials. All the machines in the library are connected in a LAN environment. The library has been using an integrated library automation package, KOHA software, to work in integrated, multi-user, and network settings. The KOHA is state-of-the-art software for holistic management of libraries that provides various modules, i.e., Acquisition, Cataloguing, Circulation, Member Facilities, Article Indexing & Abstracting, Reservation of Books, WEB OPAC, Report Generation, etc. The indexing part of KOHA software is robust. Besides this, a computerized exit module is also available. </p>

