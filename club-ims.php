                                    </div>
                                    <p class="mt-3 text-justify">The Marketing Club took a heads-off with MarkFie Expedition, Clues- Click- Content where students performed in teams to solve the clues hidden around the campus, clicked a selfie with them and posted on their social
                                        media handles. </p>

                                    <p class="mt-3 text-justify">The students had an exclusive access to club-organized events like Live Project at CavinKare, offering a distinguished edge, and access to live projects, resources, and learning. Notably, "MAUKA-MAUKA" was a live screening
                                        of the epic India-Pakistan World Cup match that brought students and faculty together, creating a thrilling showcase of a historic rivalry. The "Confusion-Crusher" event, organized by final-year students for first-year
                                        students at IMS Hostel, aimed to master the art of marketing, providing a platform for resolving doubts and excelling in their final exams.</p>
                                    <p class="mt-3 text-justify">In addition, MARKWIZ offers a diverse range of events, workshops including MarkTrek, Marketing Chakarvyuh, Brand Quest, Bidcraft where teams were challenged on various aspects of Marketing from Brand Decoding to creative
                                        representation and live selling, culminating in a thrilling MarkTrek.</p>
                                    <p class="mt-3 text-justify"> In conclusion, MARKWIZ goes the extra mile by hosting the annual academic fest “Samagam”, offering students real-world business exposure, and fostering a continuous connection with marketing insights and learning.</p>
