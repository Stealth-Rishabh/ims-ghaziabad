                                                        <h3 class="fs-5 text-blue mb-1">Process of Grievance Redressal includes the following steps</h3>

                                                        <ul>

                                                            <li><b>Step 1 - </b>All grievances to be submitted through the dedicated portal (on the IMS website) or sent through <a href="mailto:grievanceredressal@imsgzb.ac.in">e-mail to grievanceredressal@imsgzb.ac.in</a></li>

                                                            <li><b>Step 2 - </b>After the grievance is received, the Grievance Redressal Committee (GRC) meets and discusses the issue(s) of the aggrieved party and conducts a detailed investigation of the grievance.</li>

                                                            <li><b>Step 3 - </b>The Grievance Redressal Committee (GRC) will propose the possible solutions to the Director for the final decision.</li>

                                                            <li><b>Step 4 - </b>In case of dissatisfaction by the aggrieved party, the issue is referred to the Ombudsman's office. In this case, all the relevant information/documents need to be sent to the
                                                                Ombudsman.</li>

                                                            <li><b>Step 5 - </b>The final decision, whatsoever, to be communicated to the aggrieved party, and this would follow the closure of the grievance.</li>

